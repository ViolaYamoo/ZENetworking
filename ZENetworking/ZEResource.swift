class ZEResource: NSObject {

    
    class func printGrowth(golds:[Int],waters:[Int],oils:[Int]){
        if (golds.count != waters.count) || (golds.count != oils.count) || (oils.count != waters.count) || (golds.count < 2){
            print("数组个数错误")
            return
        }
        for i in 0 ..< golds.count-1 {
            print("\(golds[i + 1] - golds[i]),\(waters[i + 1] - waters[i]),\(oils[i + 1] - oils[i])")

        }
        print("\(golds.last! - golds.first!),\(waters.last! - waters.first!),\(oils.last! - oils.first!)")
    }

